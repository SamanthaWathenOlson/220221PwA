package com.suminski.playground;

public class Primitives {
    boolean b = true;
    byte by = 127; // 1 byte of data
    char c = 'c'; // notice I use single quotes here: char types use single quotes, strings double quotes. 2 bytes
    short s = 50; // 2 bytes
    int i = 10000; // 4 bytes
    float f = 100.9f; // notice I added an f at the end of the number. 4 bytes
    long l = 88888;// 8 bytes
    double d = 888888.87; // 8 bytes



}
